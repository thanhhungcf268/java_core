
public class Out {

    public static void main(String[] args) {
        byte i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char a = 'a';
        String s = "Hà Nội";
        System.out.println("i =" + i);
        System.out.println("f =" + f);
        System.out.println("d =" + d);
        System.out.println("b =" + b);
        System.out.println("a =" + a);
        System.out.println("s =" + s);

    }

}
